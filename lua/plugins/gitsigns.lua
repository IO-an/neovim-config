-- Показывает изменения Git: символы в левой колонке и подсветку номеров строк.
-- Используются ASCII-символы, работающие в любом моноширинном шрифте.
return {
  'lewis6991/gitsigns.nvim',
  event = 'VeryLazy',
  opts = {
    signs = {
      add = { text = '+' },       -- добавленные строки
      change = { text = '~' },    -- изменённые строки
      delete = { text = '-' },    -- удалённые строки
    },
    numhl = true,                 -- дополнительно подсвечивать номер строки
  },
}